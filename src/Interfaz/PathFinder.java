    private void contarMovimiento() {
        if (c > 13) {
            txtmovimiento.setText(String.valueOf(cont++));
        }
    }
    //probar

    private void ganar() {
        if (Integer.parseInt(btn1.getText()) == 1) {
            if (Integer.parseInt(btn2.getText()) == 2) {
                if (Integer.parseInt(btn3.getText()) == 3) {
                    if (Integer.parseInt(btn4.getText()) == 4) {
                        if (Integer.parseInt(btn5.getText()) == 5) {
                            if (Integer.parseInt(btn6.getText()) == 6) {
                                if (Integer.parseInt(btn7.getText()) == 7) {
                                    if (Integer.parseInt(btn8.getText()) == 8) {
                                        Win.setVisible(true);
                                        stop = 1;
                                        pausa.setVisible(false);
                                        reiniciar.setVisible(true);
                                        conti.setVisible(true);
                                        histK.setWin(0, true);
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
    }
    //probbar

    private void empezar() {
        stop = 0;
        if (c == 0) {
            ordenar();
            Win.setVisible(false);
        }
        comenzar.setVisible(false);
        reiniciar.setVisible(false);
        pausa.setVisible(true);

    }

    //probar
    private void pausa() {
        stop = 1;

        pausa.setVisible(false);
        comenzar.setVisible(true);
        reiniciar.setVisible(true);
    }

    //probar
    private void reiniciar() {
        pausa();
        tiempo = 0;
        cont = 0;
        c = 0;
        txtmovimiento.setText(String.valueOf(cont));
        txttiempo.setText(String.valueOf(tiempo));
        reiniciar.setVisible(false);
        Win.setVisible(false);

    }

