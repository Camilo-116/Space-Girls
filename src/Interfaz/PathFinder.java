  Thread hilo1 = new Thread() {
        @Override
        public void run() {
            int infinito = 0;

            while (infinito == 0) {
                try {
                    
                    if (stop == 0 && c > 11) {
                        txttiempo.setText(String.valueOf(tiempo++));
                    }
                    Thread.sleep(1000);
                } catch (InterruptedException e) {

                }
            }
        }
    };

